function add(x, y) {
return x + y;
}
function subtract(x, y) {
return x - y;
}
function multiply(x, y) {
return x * y;
}
function divide(x, y) {
return x / y;
}
function increment(number) {
    return (number += 1);
}
function decrement(number) {
    return (number -= 1);
}
function makeInt(number){
  return  parseInt(number, 10);
}
function preserveDecimal(number) {
    return parseFloat(number);
}